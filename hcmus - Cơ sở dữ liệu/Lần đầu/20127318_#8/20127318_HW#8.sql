SELECT (CASE GV.PHAI
		WHEN N'Nam' THEN YEAR(NGSINH) + 60 
		WHEN N'Nữ' THEN YEAR(NGSINH) + 55
		END) AS 'NAMDUKIENNGHIHUU'
FROM GIAOVIEN AS GV 